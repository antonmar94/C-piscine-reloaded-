int	ft_count_if(char **tab, int (*f)(char*))
{
	int	num_one;
	int	i;

	i = 0;
	num_one = 0;
	while (tab[i] != 0)
	{
		if (f(tab[i]) == 1)
			num_one++;
		i++;
	}
	return (num_one);
}
